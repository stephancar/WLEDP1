/*
 * HomeWizard P1 usermod (ESP32 only)
 *
 * Polls a HomeWizard P1 meter (Wi-Fi P1 dongle) on the local network and
 * exposes the current grid power as a WLED effect ("Grid Flow"):
 *   - offtake / import  (active_power_w > 0): purple pulses flowing in one direction
 *   - injection / export (active_power_w < 0): green pulses flowing the other way
 *   - near zero: calm white breathing
 *
 * The P1 meter is found automatically via mDNS (service _hwenergy._tcp),
 * or a fixed IP/hostname (optionally "host:port") can be set in Usermod Settings.
 *
 * Data source: HomeWizard local API v1, GET http://<host>/api/v1/data
 * ("Local API" must be enabled in the HomeWizard Energy app).
 * API reference: https://api-documentation.homewizard.com/docs/v1/
 *
 * Architecture:
 *  - All networking (mDNS discovery + HTTP polling) runs on a dedicated
 *    low-priority FreeRTOS task pinned to core 0, the same pattern the
 *    audioreactive usermod uses for its processing task. Blocking calls there
 *    cannot stall the LED pipeline on the main loop task, and there is no
 *    async client lifecycle to race on.
 *  - The task publishes plain 32-bit values (atomic on ESP32); host strings
 *    are exchanged under a FreeRTOS mutex, per docs/cpp.instructions.md.
 *  - The main loop low-pass filters the power value; the effect renders only
 *    from filtered values so all transitions are smooth.
 */

