#include "wled.h"

/*
 * HomeWizard P1 usermod
 *
 * Polls a HomeWizard P1 meter (Wi-Fi P1 dongle) on the local network and
 * exposes the current grid power as a WLED effect ("Grid Flow"):
 *   - offtake / import  (active_power_w > 0): purple pulses flowing in one direction
 *   - injection / export (active_power_w < 0): green pulses flowing the other way
 *   - near zero: calm white breathing
 *
 * The P1 meter is found automatically via mDNS (service _hwenergy._tcp),
 * or a fixed IP/hostname can be set in Usermod Settings.
 *
 * Data source: HomeWizard local API v1, GET http://<host>/api/v1/data
 * ("Local API" must be enabled in the HomeWizard Energy app).
 * API reference: https://api-documentation.homewizard.com/docs/v1/
 *
 * Implementation notes:
 *  - The HTTP request is fully asynchronous (AsyncClient); TCP callbacks only
 *    append to a buffer and set flags, all parsing and WLED state changes
 *    happen in the usermod loop() on the main task. This keeps the LED
 *    pipeline running and avoids the watchdog resets a blocking HTTPClient
 *    in loop() would cause.
 *  - mDNS discovery (MDNS.queryService) blocks for a few seconds, so it only
 *    runs while no host is known, with a retry backoff.
 *
 * Async TCP handling pattern based on usermods/usermod_v2_HttpPullLightControl
 * (Roel Broersma), effect structure based on usermods/user_fx.
 */

